def test_slugify_normalizes_messy_ids() -> None:
    assert slugify("Auth Service!!") == "auth-service"
    assert slugify("already-kebab") == "already-kebab"
    assert slugify("___") == "component"
    assert slugify("A__B") == "a-b"


def test_dedupe_slugs_appends_suffix_on_collision() -> None:
    result = dedupe_slugs(["Auth Service", "auth-service", "AUTH SERVICE"])
    assert result == {
        "Auth Service": "auth-service",
        "auth-service": "auth-service-2",
        "AUTH SERVICE": "auth-service-3",
    }


