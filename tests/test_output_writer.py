def test_write_docs_creates_expected_layout(
    config: Config,
    component_map: ComponentMap,
    component_notes: list[ComponentNotes],
    docs: SynthesizedDocs,
) -> None:
    docs_dir = write_docs(component_map, component_notes, docs, config)

    assert docs_dir == config.target_repo / "docs"
    assert (docs_dir / "architecture.md").is_file()
    assert (docs_dir / "api-reference.md").is_file()
    assert (docs_dir / "modules" / "alpha-component.md").is_file()
    assert (docs_dir / ".autodoc-harness" / "component-map.json").is_file()
    assert (docs_dir / ".autodoc-harness" / "component-notes" / "alpha-component.json").is_file()
    assert (docs_dir / ".autodoc-harness" / "run-manifest.json").is_file()


def test_written_docs_include_generated_header(
    config: Config,
    component_map: ComponentMap,
    component_notes: list[ComponentNotes],
    docs: SynthesizedDocs,
) -> None:
    docs_dir = write_docs(component_map, component_notes, docs, config)
    content = (docs_dir / "architecture.md").read_text()
    assert "GENERATED by autodoc-harness" in content
    assert "anthropic/claude-sonnet-4-5" in content
    assert "# Architecture body" in content


def test_run_manifest_hashes_visited_files(
    config: Config,
    component_map: ComponentMap,
    component_notes: list[ComponentNotes],
    docs: SynthesizedDocs,
) -> None:
    docs_dir = write_docs(component_map, component_notes, docs, config)
    manifest = json.loads((docs_dir / ".autodoc-harness" / "run-manifest.json").read_text())
    assert "alpha.py" in manifest["files"]
    assert len(manifest["files"]["alpha.py"]) == 64  # sha256 hex digest length


def test_rerun_clears_stale_module_docs(
    config: Config,
    component_map: ComponentMap,
    component_notes: list[ComponentNotes],
    docs: SynthesizedDocs,
) -> None:
    docs_dir = write_docs(component_map, component_notes, docs, config)
    assert (docs_dir / "modules" / "alpha-component.md").is_file()

    # Simulate a second run where the "alpha" component no longer exists.
    empty_map = component_map.model_copy(update={"components": []})
    empty_docs = SynthesizedDocs(
        architecture_md="# New architecture", api_reference_md="# New api", module_docs={}
    )
    write_docs(empty_map, [], empty_docs, config)

    assert not (docs_dir / "modules" / "alpha-component.md").exists()
    assert not (docs_dir / ".autodoc-harness" / "component-notes" / "alpha-component.json").exists()
    assert "New architecture" in (docs_dir / "architecture.md").read_text()
